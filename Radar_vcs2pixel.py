import numpy as np


def vcs2pixel(distlong, distlat):
  matrix = np.array([[-1.448681215609537e-04, 1.911017954443175e-04, 4.818633653503788e-04, -0.684761724961685],
                    [-8.426235925231896e-05, -3.557413573975637e-06, 5.566131304022677e-04, -0.728766510083092],
                    [-7.420172194445938e-08, -1.132573701490003e-09, 2.655637855175641e-07, -3.836388711560557e-04]])

  pos = np.array([[distlong * 1000], [distlat * 1000], [750], [1]])
  pos_p = np.dot(matrix, pos)

  u = int(pos_p[0] / pos_p[2])
  v = int(pos_p[1] / pos_p[2])

  return u, v
